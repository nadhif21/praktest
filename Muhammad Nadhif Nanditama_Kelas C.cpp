#include <iostream>
using namespace std;


int main(){
	cout <<"MASUKAN ANGKA \n" << "\n";
	
	int x;
	cout<<"angka pertama :";
	cin>>x;
	
	int y;
	cout<<"angka kedua :";
	cin>>y;
	
	cout <<"memiliki hasil :\n" << "\n";
	
	cout <<"jika ditambah adalah " << x + y << endl;
	
	cout <<"jika dikurangi adalah " << x - y << endl;
	
	cout <<"jika dikali adalah " << x * y << endl;
	
	cout <<"jika dibagi adalah " << x / y << endl;
	
	cout <<"dan sisa baginya adalah " << x % y << endl;
	
	return 0;
	
}
